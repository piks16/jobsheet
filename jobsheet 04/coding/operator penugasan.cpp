#include <stdio.h>
int main (){
	int a=10,b=5;
	b+=a;
	printf("hasil b+=a adalah %d\n", b);
	b-=a;
	printf("hasil b-=a adalah %d\n", b);
	b*=a;
	printf("hasil b*=a adalah %d\n", b);
	b/=a;
	printf("hasil b/=a adalah %d\n", b);
	b%=a;
	printf("hasil b%=a adalah %d\n", b);
	return 0;
}
